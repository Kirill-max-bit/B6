def найти_кратное_семи(последовательность):
    """
    Находит первое число в последовательности, кратное семи
    
    Args:
        последовательность: список целых чисел, оканчивающийся на -1
        
    Returns:
        tuple: (найдено, позиция)
               где найдено - True если число найдено, False если нет
                     позиция - индекс первого числа, кратного 7
                             или -1 если такое число не найдено
    """
    # Проверяем, что последовательность заканчивается на -1
    if последовательность[-1] != -1:
        raise ValueError("Последовательность должна оканчиваться на -1")
    
    for i, число in enumerate(последовательность[:-1]):  # Не проверяем последний элемент (-1)
        if число % 7 == 0:
            return (True, i + 1)  # +1 для порядкового номера с 1
    return (False, -1)

# Примеры использования
примеры = [
    [14, 21, 35, 49, 63, 77, 91, 105, 119, -1],
    [1, 2, 3, 4, 5, 6, 8, 9, 10, -1],
    [7, 14, 21, 28, 35, 42, 49, 56, 63, -1]
]

for i, последовательность in enumerate(примеры):
    найдено, позиция = найти_кратное_семи(последовательность)
    print(f"\nПоследовательность {i+1}: {последовательность}")
    if найдено:
        print(f"Найдено число на позиции {позиция}")
    else:
        print("Чисел, кратных семи, не найдено")
