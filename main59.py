# Заданная последовательность вещественных чисел
sequence = [1.5, 2.3, -0.4, 3.7, 4.1, 5.6, -2.8, 6.9, 7.2, 8.4, 9.1, 10.5, 11.3, 12.7, 13.8]

# Флаг для проверки наличия отрицательных чисел
found = False

# Проходим по последовательности
for i, num in enumerate(sequence, start=1):
    if num < 0:
        print(f"Первое отрицательное число найдено на позиции {i}.")
        found = True
        break

# Если отрицательных чисел не найдено
if not found:
    print("В последовательности нет отрицательных чисел.")
