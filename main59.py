def найти_первое_77(последовательность):
    """
    Находит первый индекс числа 77 в последовательности,
    если оно существует, иначе возвращает -1
    
    Args:
        последовательность: список целых чисел, заканчивающийся 100
        
    Returns:
        int: индекс первого числа 77 или -1, если число не найдено
    """
    try:
        return последовательность.index(77)
    except ValueError:
        return -1

# Примеры использования
примеры = [
    [1, 77, 88, 99, 100],
    [1, 2, 3, 100],
    [77, 77, 77, 100],
    [1, 88, 77, 99, 100]
]

for i, последовательность in enumerate(примеры):
    результат = найти_первое_77(последовательность)
    print(f"Последовательность {i+1}: {последовательность}")
    print(f"Результат: {результат}\n")
