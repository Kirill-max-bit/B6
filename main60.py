def найти_число_на_семерку(последовательность):
    """
    Находит первое число в последовательности, оканчивающееся на 7
    
    Args:
        последовательность: список из 20 натуральных чисел
        
    Returns:
        tuple: (найдено, позиция)
               где найдено - True если число найдено, False если нет
                     позиция - индекс первого числа, оканчивающегося на 7
                             или -1 если такое число не найдено
    """
    for i, число in enumerate(последовательность):
        if str(число)[-1] == '7':
            return (True, i + 1)  # +1 для порядкового номера с 1
    return (False, -1)

# Примеры использования
примеры = [
    [1, 27, 38, 49, 50, 61, 72, 83, 94, 105, 116, 127, 138, 149, 150, 161, 172, 183, 194, 205],
    [1, 2, 3, 4, 5, 6, 8, 9, 10, 11, 12, 13, 14, 15, 16, 18, 19, 20, 21, 22],
    [17, 27, 37, 47, 57, 67, 77, 87, 97, 107, 117, 127, 137, 147, 157, 167, 177, 187, 197, 207]
]

for i, последовательность in enumerate(примеры):
    найдено, позиция = найти_число_на_семерку(последовательность)
    print(f"\nПоследовательность {i+1}: {последовательность}")
    if найдено:
        print(f"Найдено число на позиции {позиция}")
    else:
        print("Чисел, оканчивающихся на 7, не найдено")
